package pt.c02oo.s05encapsulamento.s20polynom;

public class TermoB {
   private int a, k;
   
   public TermoB(int a, int k) {
      this.a = a;
      this.k = k;
   }
   
   public int getA() {
      return a;
   }
   public void setA(int a) {
      this.a = a;
   }
   
   public int getK() {
      return k;
   }
   public void setK(int k) {
      this.k = k;
   }
}
